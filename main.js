var daynum = prompt("Enter Number")

switch(daynum){
    case '1' : alert(`Saturday`)
    break;
    case '2' : alert(`Sunday`)
    break;
    case '3' : alert(`Monday`)
    break;
    case '4' : alert(`Tuesday`)
    break;
    case '5' : alert(`Wednesday`)
    break;
    case '6' : alert(`Thursday`)
    break;
    case '7' : alert(`Friday`)
    break;
    default : alert(`Enter invalid number`)
}